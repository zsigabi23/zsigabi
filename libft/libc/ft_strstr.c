#include "libft.h"

void	*ft_strstr(const char *s1, const char *s2)
{
	int	i;
	int	z;

	i = 0;
	z = 0;
	if (s2[i] == 0)
		return ((char*)s1);
	while (s1[i] != '\0')
	{
		while (s2[z] == s1[i + z])
		{
			if (s2[i + z] != '\0')
			{
				return ((char*)s1 + i);
			}
			z++;
		}
		i++;
	}
	return (0);
}
