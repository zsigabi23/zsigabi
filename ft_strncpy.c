#include "libft.h"

void	*ft_strncpy(char *s1, char *s2, size_t n)
{
	int	i;

	i = 0;
	while (i < n && s2[i])
	{
		s1[i] = s2[i];
		i++;
	}
	s1[i] = '\0';
	return (s1);
}
