#include "libft.h"

void	*ft_strncat(char *s1, const char *s2, size_t n)
{
	size_t	i;
	size_t	z;

	i = 0;
	z = 0;
	while (s1[i] != '\0')
		i++;
	while (z < n && s2[z])
	{
		s1[i + z] = s2[z];
		z++;
	}
	s1[i + z] = '\0';
	return (s1);
}
