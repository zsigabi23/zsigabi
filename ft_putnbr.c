#include <stdio.h>
#include <string.h>

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		putstr("-2147483648");
	else
	{
		if (n < 0)
		{
			putchar('-');
			n *= -1;
		}
		if (n >= 10)
			ft_putnbr(n / 10);
		ft_putchar((n % 10) + '0');
	}
}

int		main()
{
	ft_putnbr(123);
	return (0);
}
